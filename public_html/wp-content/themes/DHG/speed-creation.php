    // Modo dark automático (sin botón)
    (function() {
        const root = document.documentElement;
        const webcrumbs = document.getElementById('webcrumbs');
        const storageKey = 'dhg-dark-mode';

        function setDark(on) {
            if (on) {
                root.classList.add('dark');
                webcrumbs.classList.add('dark');
                localStorage.setItem(storageKey, '1');
            } else {
                root.classList.remove('dark');
                webcrumbs.classList.remove('dark');
                localStorage.setItem(storageKey, '0');
            }
        }
        // Inicializar según preferencia
        if (localStorage.getItem(storageKey) === '1' || (localStorage.getItem(storageKey) === null && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
            setDark(true);
        }
    })();
    // Animación de barras de progreso y SVG circulares
    window.addEventListener('DOMContentLoaded', function() {
        // Barras horizontales
        document.querySelectorAll('.progress-bar').forEach(function(bar) {
            bar.style.width = '0%';
            setTimeout(function() {
                bar.style.width = bar.getAttribute('data-width') + '%';
            }, 200);
        });
        // SVG circulares
        document.querySelectorAll('.stat-circle').forEach(function(circle) {
            const percent = parseInt(circle.getAttribute('data-percentage'), 10);
            const radius = circle.getAttribute('r');
            const circumference = 2 * Math.PI * radius;
            circle.style.strokeDasharray = circumference;
            circle.style.strokeDashoffset = circumference;
            setTimeout(function() {
                circle.style.transition = 'stroke-dashoffset 1.2s cubic-bezier(.4,1.7,.6,1)';
                circle.style.strokeDashoffset = circumference * (1 - percent / 100);
            }, 300);
        });
        // Números animados
        document.querySelectorAll('.stat-number').forEach(function(num) {
            const target = parseInt(num.getAttribute('data-target'), 10);
            let current = 0;
            const step = Math.max(1, Math.round(target / 40));

            function animate() {
                if (current < target) {
                    current += step;
                    if (current > target) current = target;
                    num.textContent = current + '%';
                    requestAnimationFrame(animate);
                } else {
                    num.textContent = target + '%';
                }
            }
            setTimeout(animate, 500);
        });
        // Tooltips animados
        document.querySelectorAll('.animate-tooltip').forEach(function(tip) {
            tip.style.opacity = 0;
            tip.style.transform = 'translateY(8px)';
            tip.parentElement.parentElement.addEventListener('mouseenter', function() {
                tip.style.transition = 'opacity 0.3s, transform 0.3s';
                tip.style.opacity = 1;
                tip.style.transform = 'translateY(0)';
            });
            tip.parentElement.parentElement.addEventListener('mouseleave', function() {
                tip.style.opacity = 0;
                tip.style.transform = 'translateY(8px)';
            });
        });
    });
</script>

